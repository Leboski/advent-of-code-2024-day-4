package main

import (
	"time"
)

func main() {
	start := time.Now()
	toMatrix := [][]string{}
	line := []string{}

	data := realInput()
	lenData := len(data)
	for i, letter := range data {
		if letter == '\n' {
			toMatrix = append(toMatrix, line)
			line = []string{}
			continue
		}

		line = append(line, string(letter))

		if i == lenData-1 {
			toMatrix = append(toMatrix, line)
			break
		}
	}

	count := 0
	for i, row := range toMatrix {
		for j, letter := range row {
			if letter != "A" {
				continue
			}

			has4Sides := i > 0 && i < len(toMatrix)-1 && j > 0 && j < len(row)-1
			if !has4Sides {
				continue
			}

			side1 := toMatrix[i-1][j-1]
			side2 := toMatrix[i-1][j+1]
			side3 := toMatrix[i+1][j-1]
			side4 := toMatrix[i+1][j+1]

			if side1 == "M" && side2 == "S" && side3 == "M" && side4 == "S" {
				count++
			}

			if side1 == "S" && side2 == "M" && side3 == "S" && side4 == "M" {
				count++
			}

			if side1 == "S" && side2 == "S" && side3 == "M" && side4 == "M" {
				count++
			}

			if side1 == "M" && side2 == "M" && side3 == "S" && side4 == "S" {
				count++
			}
		}
	}

	duration := time.Since(start)
	if count == 0 {
		println("X-MAS not found")
	} else {
		println("X-MAS found", count, "times")
	}
	println("Execution time", duration.Microseconds(), "Âµs")
}

